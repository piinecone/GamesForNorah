/**
 * Scans horse-stable/music/ and writes src/audio/playlist.generated.ts
 * Run: node scripts/generate-playlist.mjs
 */
import { existsSync, mkdirSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const musicDir = join(__dirname, '..', 'music');
const outDir = join(__dirname, '..', 'src', 'audio');
mkdirSync(outDir, { recursive: true });
const outFile = join(outDir, 'playlist.generated.ts');

const tracks = existsSync(musicDir)
  ? readdirSync(musicDir)
      .filter((name) => /\.(mp3|ogg|wav|m4a)$/i.test(name))
      .sort((a, b) => a.localeCompare(b))
      .map((name) => `./music/${encodeURIComponent(name)}`)
  : [];

writeFileSync(
  outFile,
  `// Auto-generated by scripts/generate-playlist.mjs — do not edit\nexport const MUSIC_PLAYLIST: readonly string[] = ${JSON.stringify(tracks, null, 2)};\n`,
);
console.log(`Wrote ${outFile} (${tracks.length} track${tracks.length === 1 ? '' : 's'})`);
