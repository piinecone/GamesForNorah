export const needKeys: NeedKey[] = ['hunger', 'affection', 'cleanliness', 'exercise'];

export function t(language: Language, key: string, vars: Record<string, string | number> = {}): string {
  const template = dictionaries[language][key] ?? dictionaries.eu[key] ?? key;
  return Object.entries(vars).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template,
  );
}
